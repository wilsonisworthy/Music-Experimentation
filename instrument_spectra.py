"""Per-instrument harmonic spectra derived from the SHARC timbre database.

Source: Gregory Sandell's Harmonic Archive (SHARC), via the
anton-k/sharc-timbre port (https://github.com/anton-k/sharc-timbre),
itself built from analyses of the McGill/Iowa recordings used in Sandell's
1991 dissertation. For each instrument, the spectrum below is measured on
a note near the middle of its recorded range (fundamental frequency and
pitch name given by INSTRUMENT_SOURCE_NOTES) -- not the lowest or highest
note, since timbre (and thus the harmonic profile) shifts across an
instrument's range.

Frequencies are given as integer harmonic multiples of the fundamental
(1 = fundamental, 2 = first overtone, ...) rather than absolute Hz, so a
spectrum can be retuned to any base pitch. Amplitudes are the measured
harmonic magnitudes, normalized so the loudest of the kept harmonics is 1.0.
Each spectrum is truncated to its 16 strongest-region harmonics (1-16); for
every instrument here, the single loudest harmonic falls within that range,
so the truncation does not discard the dominant partial.

Each entry is a (frequencies, amplitudes) pair -- the same spectrum format
accepted by sethares_dissonance() in dissonance_functions.py. To use one:

    from dissonance_functions import sethares_dissonance
    from instrument_spectra import INSTRUMENT_SPECTRA, scaled_spectrum

    note_a = scaled_spectrum("Oboe", 440.0)
    note_b = scaled_spectrum("Clarinet", 440.0 * 2 ** (7 / 12))
    sethares_dissonance(note_a, note_b)
"""

