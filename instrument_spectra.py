def scaled_spectrum(instrument: str, base_freq: float) -> Tuple[List[float], List[float]]:
    """Return (frequencies, amplitudes) for `instrument`'s spectrum retuned to `base_freq`.

    Multiplies each harmonic's integer multiple by `base_freq` to get absolute
    Hz, ready to pass (alone or alongside other notes) to sethares_dissonance.
    """
    multiples, amplitudes = INSTRUMENT_SPECTRA[instrument]
    return [m * base_freq for m in multiples], list(amplitudes)

